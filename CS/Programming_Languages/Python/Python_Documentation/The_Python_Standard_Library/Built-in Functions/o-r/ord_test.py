"""Given a string representing one Unicode character, return an integer representing the Unicode code point
of that character. For example, ord('a') returns the integer 97 and ord('Ð‚') (Euro sign) returns 8364.
This is the inverse of chr()."""

# Example 1: How ord() works in python?
# code point of integer
print(f"ord('5'): {ord('5')}")

# code point of alphabet
print(f"ord('A'): {ord('A')}")

# code point of character
print(f"ord('$'): {ord('$')}")